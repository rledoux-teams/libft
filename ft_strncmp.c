#include <stddef.h>

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (s1[i] == s2[i] && i < n)
		i++;
	if (i == n || s1[i] == s2[i])
		return (0);
	if (s1[i] > s2[i] || s1[i] == '\200')
		return (1);
	return (-1);
}
