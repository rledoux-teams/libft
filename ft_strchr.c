#include <stdlib.h>

char	*ft_strchr(const char *s, int c)
{
	if (c == 0)
	{
		while (*s != '\0')
			s++;
		return ((char *)s);
	}
	while (*s != '\0')
	{
		if (*s == c)
			return ((char *)s);
		s++;
	}
	return (0);
}
